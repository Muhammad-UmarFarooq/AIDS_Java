public class PrintAscii_ex2_m1
{
    public static void main(String args[])
    {
        // Characters whose ASCII value to be found
        char c1='a';
        char c2='b';
        //Casting or converting a character into int type
        int ascii1=(int)c1;
        float ascii2=(float)c2;
        System.out.println("The ASCII value of "+c1+" is: "+ascii1);
        System.out.println("The ASCII value of "+c2+" is: "+ascii2);
    }
}
